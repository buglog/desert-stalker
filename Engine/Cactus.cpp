#include "Cactus.h"

void Cactus::init(int setx, int sety, int setVersion)
{
	x = setx;
	y = sety;
	version = setVersion;
}

void Cactus::place(Graphics& gfx, Vampire& vamp, Instructions& inst)
{
	if (version == 0)
	{
		width = 30;
		height = 50;
		update(vamp,inst);
		drawVer0(gfx);
		cWall.init(x, y, x + width, y + height, Colors::Black, 1);
		cWall.block(vamp);
	}
	else if (version == 1)
	{
		//set width and height
		drawVer1(gfx);
	}
	else if (version == 2)
	{
		//set width and height
		drawVer2(gfx);
	}
}

void Cactus::update(Vampire& vamp, Instructions& inst)
{
	if (vamp.x + vamp.width >= x - scareRange &&
		vamp.x <= x + width + scareRange &&
		vamp.y + vamp.height >= y - scareRange &&
		vamp.y <= y + height + scareRange)
	{
		inst.e_message = inst.terrorize;
		if (vamp.isScary)
		{
			isScared = true;
		}
	}
	turnWhite();
}

void Cactus::turnWhite()
{
	//increment colCounter every frame
	if (isScared)
	{
		if (r < 255)
		{
			r += turnWhiteSpeed;
			if (r > 255)
			{
				r = 255;
			}
		}
		if (g < 255)
		{
			g += turnWhiteSpeed;
			if (g > 255)
			{
				g = 255;
			}
		}
		if (b < 255)
		{
			b += turnWhiteSpeed;
			if (b > 255)
			{
				b = 255;
			}
		}
		if (r >= 255 && g >= 255 && b >= 255)
		{
			isScared = false;
		}
	}
	else
	{
		colCounter += 1;
		//turn white fast, turn green slow 
		if (colCounter >= turnGreenSpeed)
		{
			colCounter = 0;
			if (r > 180)
			{
				--r;
				if (r < 180)
				{
					r = 180;
				}
			}
			if (g > 210)
			{
				--g;
				if (g < 210)
				{
					g = 210;
				}
			}
			if (b > 70)
			{
				--b;
				if (b < 70)
				{
					b = 70;
				}
			}
			if (r <= 180 && g <= 210 && b <= 70)
			{
				isScared = false;
			}
		}
	}
}

