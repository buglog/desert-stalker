#include "ProgressBar.h"

void ProgressBar::init(int setx, int sety, Color setc, int setType, float setP)
{
	x = setx;
	y = sety;
	c = setc;
	setProg = setP;
	type = setType;
	frame.init(x, y + 30, x + width, y + 31 + height);
}

void ProgressBar::draw(Graphics & gfx,Message& msg)
{
	// make sure progress doesn't exceed bar
	if (int(setProg) > width)
	{
		setProg = float(width);
		// set message if progress is high.
		if (type == 1)
		{
			msg.message = msg.truly_terrifying;
			msg.reset();
		}
	}
	else if (setProg <= 1.0f)
	{
		setProg = 1.0f;
	}
	// ease in and out
	if (progress < setProg)
	{
		++progress;
	}
	else if (progress > setProg)
	{
		--progress;
	}
	if (type == 0 && progress >= width - 2)
	{
		if (!cowboyMsgDone)
		{
			msg.message = msg.true_cowboy;
			msg.reset();
			cowboyMsgDone = true;
		}
	}
	// set progress bar message
	if (type == 0)
	{
		draw_cowboy(gfx);
	}
	else if (type == 1)
	{
		draw_terror(gfx);
	}
	int x_bar = x;
	int y_bar = y + 30;
	cFrame.SetR(255);
	cFrame.SetG(255);
	cFrame.SetB(0);
	gfx.Rectangle(x_bar, y_bar, x_bar + int(progress), y_bar + height, 2, c);
	frame.draw(1, cFrame, gfx);
}

void ProgressBar::incr(int n)
{
	setProg = setProg + float(n);
}

int ProgressBar::getProg()
{
	return int(progress);
}

