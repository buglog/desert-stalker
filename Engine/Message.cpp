#include "Message.h"

Message::Message()
{
	fmsg.init(260, 520, 720, 560);
	counter = msgTime;
}

void Message::draw(Graphics & gfx)
{
	if (counter < msgTime)
	{
		++counter;
		if (message == cactus_scared)
		{
			draw_frame(gfx);
			draw_cactScared(gfx);
		}
		else if (message == got_hat)
		{
			draw_frame(gfx);
			draw_gotHat(gfx);
		}
	}
}

void Message::reset()
{
	counter = 0;
}

void Message::draw_frame(Graphics & gfx)
{
	//draw rect, then frame
	gfx.Rectangle(fmsg.x, fmsg.y, fmsg.width, fmsg.height, 1, Colors::Black);
	cframe.SetR(255);
	fmsg.draw(1, cframe, gfx);
}

